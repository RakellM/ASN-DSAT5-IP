# Create a program that receives an integer and calculates its square root
# and displays the result.

# %%
print("This is a program that calculates the square root of a integer number.")
number = int(float(input("Enter an integer number: ")))

square_root = number ** (1/2)

print(f"The square root of {number} is {square_root}.")
